public class SelfGugu1{ 
	public static void main(String [] args){
	
		for(int i=1;i<10;i++){ 
			for (int d=2;d<10;d++){
				System.out.print( d + "x" + i + "=" + d * i + " ");
				}
					System.out.println();
		}

		for(int d=2;d<10;d++){
			for(int i=1;i<10;i++){
				System.out.print( i + "x" + d + "=" + i * d + " " );
				}
					System.out.println();
		}
	}	


}